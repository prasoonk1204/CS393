// WAP in java to find the sum of 2 nos. where the nos. are taken by command line
class P5 {
    public static void main(String a[]) {
        int x = Integer.parseInt(a[0]);
        int y = Integer.parseInt(a[1]);
        int z = x+y;
        System.out.println("The sum is "+z);
    }
}

// javac P5.java
// java P5 10 20
// The sum is 30
