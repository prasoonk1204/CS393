# Loops

for i in range(10):
    print(i, end = ", ")
print()

for i in range(0, 10):
    print(i, end = ", ")
print()

for i in range(0, 10, 1):
    print(i, end = ", ")
print()

for i in range(0, 10, 2):
    print(i, end = ", ")



for i in range(10, 0, -1):
    print(i, end = ", ")
print()
for i in range(10, 0, -2):
    print(i, end = ", ")
print()



for i in range(1, 10, 2):
    print("i =", i)
else:
    print("ELSE block is executing...")
print("End of the program...")