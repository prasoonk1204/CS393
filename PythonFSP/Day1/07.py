# Operators: Unary, Binary, Ternary

# Unary Operators: Operate on single operand: +100. -100
# Binary Operators: Operate on two operands: 100 + 200, 800 - 200
# Ternary Operator: Operate on three operands: a if condition else b


# Ternary Operator
num1 = 100
result = "Even" if (num1 % 2 == 0) else "Odd"
print(num1, "is", result)

num2 = 105
result = "Even" if (num2 % 2 == 0) else "Odd"
print(num2, "is", result)


# String Operators

